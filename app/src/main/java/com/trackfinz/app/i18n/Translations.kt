private val translations: Map<AppLanguage, Map<String, String>> = mapOf(
    AppLanguage.ENGLISH    to en,
    AppLanguage.KHMER      to kh,
    AppLanguage.FRENCH     to fr,
    AppLanguage.VIETNAMESE to vi,
    AppLanguage.LAO        to lo,
    AppLanguage.CHINESE    to zh,
    AppLanguage.JAPANESE   to ja,
    AppLanguage.KOREAN     to ko,
    AppLanguage.MYANMAR    to my,
    AppLanguage.MALAY      to ms,
    AppLanguage.INDONESIAN to id,
)

/**
 * Returns the translated string for [key] in [language].
 * Falls back to English if the key is missing in the target language.
 */
fun translate(key: String, language: AppLanguage): String =
    translations[language]?.get(key)
        ?: translations[AppLanguage.ENGLISH]?.get(key)
        ?: key

/** Returns the 12 short month names translated for [language] (index 0 = January). */
fun translatedShortMonths(language: AppLanguage): List<String> {
    val keys = listOf(
        Str.MONTH_JAN, Str.MONTH_FEB, Str.MONTH_MAR, Str.MONTH_APR,
        Str.MONTH_MAY, Str.MONTH_JUN, Str.MONTH_JUL, Str.MONTH_AUG,
        Str.MONTH_SEP, Str.MONTH_OCT, Str.MONTH_NOV, Str.MONTH_DEC
    )
    return keys.map { translate(it, language) }
}

/** Returns the 12 full month names translated for [language] (index 0 = January). */
fun translatedFullMonths(language: AppLanguage): List<String> {
    val keys = listOf(
        Str.MONTH_FULL_JAN, Str.MONTH_FULL_FEB, Str.MONTH_FULL_MAR, Str.MONTH_FULL_APR,
        Str.MONTH_FULL_MAY, Str.MONTH_FULL_JUN, Str.MONTH_FULL_JUL, Str.MONTH_FULL_AUG,
        Str.MONTH_FULL_SEP, Str.MONTH_FULL_OCT, Str.MONTH_FULL_NOV, Str.MONTH_FULL_DEC
    )
    return keys.map { translate(it, language) }
}

